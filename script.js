let musica = document.querySelector('.som audio');
let indexMusica = 0;

let duracaoMusica = document.querySelector('.fim');
let nomeMusica = document.querySelector('.descricao h2');

duracaoMusica.textContent = segundosMinutos(Math.floor(musica.duration));

document.querySelector('.botao-play').addEventListener('click', tocarMusica);
document.querySelector('.botao-pause').addEventListener('click', pararMusica);
document.querySelector('.anterior').addEventListener('click', () =>{
    indexMusica--;
    if(indexMusica < 0){
        indexMusica = 99;
    }
    rendereizarMusica(indexMusica);
    document.querySelector('.botao-pause').style.display = 'none';
    document.querySelector('.botao-play').style.display = 'inline-blocks';
})
document.querySelector('.proximo').addEventListener('click', () =>{
    indexMusica++;
    if(indexMusica > 99){
        indexMusica = 0;
    }
    rendereizarMusica(indexMusica);
    document.querySelector('.botao-pause').style.display = 'none';
    document.querySelector('.botao-play').style.display = 'inline-block';
})

musica.addEventListener('timeupdate', atualizarBarra);

function rendereizarMusica(index){
    musica.setAttribute('src', musicas[index].src);
    musica.addEventListener('loadeddata', () => {
        nomeMusica.textContent = musicas[index].titulo;
        duracaoMusica.textContent = segundosMinutos(Math.floor(musica.duration));
    });
}

function tocarMusica() {
    musica.play();
    document.querySelector('.botao-pause').style.display = 'inline-block';
    document.querySelector('.botao-play').style.display = 'none';
}

function pararMusica() {
    musica.pause();
    document.querySelector('.botao-play').style.display = 'inline-block';
    document.querySelector('.botao-pause').style.display = 'none';
}

function atualizarBarra(){
    let barra = document.querySelector('progress');
    barra.style.width = Math.floor((musica.currentTime / musica.duration) * 100) + '%'; 
    let tempoDecorrido = document.querySelector('.inicio');
    tempoDecorrido.textContent = segundosMinutos(Math.floor(musica.currentTime));
}

function segundosMinutos(segundo){
    let campoMinuto = Math.floor(segundo / 60);
    let campoSegundo = segundo % 60;
    if(campoSegundo < 10 ){
        campoSegundo = '0' + campoSegundo;
    }

    return campoMinuto+':'+campoSegundo;
}

